package net.gtaun.wl.common;

public final class UnitUtils
{
	public static float kmToMi(float km)
	{
		return km * 0.621371192f;
	}
	
	public static double kmToMi(double km)
	{
		return km * 0.621371192d;
	}
	
	public static float meterToYard(float m)
	{
		return m * 1.0936133f;
	}
	
	public static double meterToYard(double m)
	{
		return m * 1.0936133d;
	}
	
	private UnitUtils()
	{
		
	}
}
